package com.github.wshackle.fanuc.robotserver  ;

import com4j.*;

/**
 * <p>
 * An enumeration of constants used to identify the sub-type of a TP program (FRCTPProgram) object.
 * </p>
 */
public enum FRETPSubTypeConstants {
  /**
   * <p>
   * No program sub type.
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  frNoneTPSubType, // 0
  /**
   * <p>
   * Job program sub type.
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  frJobTPSubType, // 1
  /**
   * <p>
   * Process program sub type.
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  frProcessTPSubType, // 2
  /**
   * <p>
   * Macro program sub type.
   * </p>
   * <p>
   * The value of this constant is 3
   * </p>
   */
  frMacroTPSubType, // 3
  /**
   * <p>
   * Condition program sub type.
   * </p>
   * <p>
   * The value of this constant is 4
   * </p>
   */
  frConditionTPSubType, // 4
  /**
   * <p>
   * Pallet program sub type.
   * </p>
   * <p>
   * The value of this constant is 5
   * </p>
   */
  frPalletTPSubType, // 5
  /**
   * <p>
   * Panel program sub type.
   * </p>
   * <p>
   * The value of this constant is 6
   * </p>
   */
  frPanelTPSubType, // 6
  /**
   * <p>
   * Collection program sub type.
   * </p>
   * <p>
   * The value of this constant is 7
   * </p>
   */
  frCollectionTPSubType, // 7
}
